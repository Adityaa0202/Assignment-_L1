
// Q3 How much space does following data types take ?

// 1.int
// 2.bool
// 3.float

#include <iostream>
using namespace std;
int main(){
  cout<<"size of int:"<<sizeof(int)<<endl;
  cout<<"size of bool:"<<sizeof(bool)<<endl;
  cout<<"size of float:"<<sizeof(float)<<endl;
}

// output:

// size of int:4
// size of bool:1
// size of float:4